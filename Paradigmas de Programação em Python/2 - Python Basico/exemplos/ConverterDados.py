### Exemplo de Código em Python:
# Leitura de dados como strings
idade_str = "25"
altura_str = "1.75"
# Conversão de tipos
idade = int(idade_str) # Converte String para Inteiro
altura = float(altura_str) # Converte String para Float
# Processamento usando os novos tipos de dados
mensagem = "Idade: " + str(idade) + ", Altura: " + str(altura)
# Exibindo a mensagem
print(mensagem) # Exibe: Idade: 25, Altura: 1.75